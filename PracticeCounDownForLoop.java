//Create a Java program that uses a while loop to print a countdown from 5 to 1. The output should be: 5, 4, 3, 2, 1

package greek;

public class PracticeCounDownForLoop {

	public static void main(String[] args)
	{
		int i=5;
		while(i>=1)
		{
			System.out.println(i);
			i--;
		}
	}
}
